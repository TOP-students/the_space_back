    // Ð¡Ð¾Ð·Ð´Ð°Ñ‚ÑŒ HTML ÑÑ‚Ñ€ÑƒÐºÑ‚ÑƒÑ€Ñƒ picker
    createPicker() {
        const picker = document.createElement('div');
        picker.className = 'emoji-picker';
        picker.id = 'emoji-picker';

        // Ð—Ð°Ð³Ð¾Ð»Ð¾Ð²Ð¾Ðº Ñ ÐºÐ°Ñ‚ÐµÐ³Ð¾Ñ€Ð¸ÑÐ¼Ð¸
        const header = document.createElement('div');
        header.className = 'emoji-picker-header';

        Object.keys(this.categories).forEach(categoryKey => {
            const category = this.categories[categoryKey];
            const btn = document.createElement('button');
            btn.className = 'emoji-category-btn';
            btn.dataset.category = categoryKey;
            btn.textContent = category.icon;
            btn.title = category.name;

            if (categoryKey === this.currentCategory) {
                btn.classList.add('active');
            }

            btn.onclick = () => this.switchCategory(categoryKey);
            header.appendChild(btn);
        });

        // ÐšÐ¾Ð½Ñ‚ÐµÐ¹Ð½ÐµÑ€ Ð´Ð»Ñ ÑÐ¼Ð¾Ð´Ð·Ð¸
        const content = document.createElement('div');
        content.className = 'emoji-picker-content';
        content.id = 'emoji-picker-content';

        picker.appendChild(header);
        picker.appendChild(content);

        return picker;
    }

    // ÐŸÐµÑ€ÐµÐºÐ»ÑŽÑ‡Ð¸Ñ‚ÑŒ ÐºÐ°Ñ‚ÐµÐ³Ð¾Ñ€Ð¸ÑŽ
    switchCategory(categoryKey) {
        this.currentCategory = categoryKey;

        // ÐžÐ±Ð½Ð¾Ð²Ð¸Ñ‚ÑŒ Ð°ÐºÑ‚Ð¸Ð²Ð½ÑƒÑŽ ÐºÐ½Ð¾Ð¿ÐºÑƒ
        document.querySelectorAll('.emoji-category-btn').forEach(btn => {
            btn.classList.toggle('active', btn.dataset.category === categoryKey);
        });

        // ÐžÑ‚Ð¾Ð±Ñ€Ð°Ð·Ð¸Ñ‚ÑŒ ÑÐ¼Ð¾Ð´Ð·Ð¸ ÐºÐ°Ñ‚ÐµÐ³Ð¾Ñ€Ð¸Ð¸
        this.renderEmojis();
    }

    // ÐžÑ‚Ð¾Ð±Ñ€Ð°Ð·Ð¸Ñ‚ÑŒ ÑÐ¼Ð¾Ð´Ð·Ð¸ Ñ‚ÐµÐºÑƒÑ‰ÐµÐ¹ ÐºÐ°Ñ‚ÐµÐ³Ð¾Ñ€Ð¸Ð¸
    renderEmojis() {
        const content = document.getElementById('emoji-picker-content');
        if (!content) return;

        content.innerHTML = '';

        const category = this.categories[this.currentCategory];
        const title = document.createElement('div');
        title.className = 'emoji-category-title';
        title.textContent = category.name;
        content.appendChild(title);

        const grid = document.createElement('div');
        grid.className = 'emoji-grid';

        category.emojis.forEach(emoji => {
            const btn = document.createElement('button');
            btn.className = 'emoji-btn';
            btn.textContent = emoji;
            btn.onclick = () => this.selectEmoji(emoji);
            grid.appendChild(btn);
        });

        content.appendChild(grid);
    }

    // Ð’Ñ‹Ð±Ñ€Ð°Ñ‚ÑŒ ÑÐ¼Ð¾Ð´Ð·Ð¸
    selectEmoji(emoji) {
        if (this.onEmojiSelect) {
            this.onEmojiSelect(emoji);
        }
        this.close();
    }

    // ÐžÑ‚ÐºÑ€Ñ‹Ñ‚ÑŒ picker
    open() {
        const picker = document.getElementById('emoji-picker');
        const btn = document.getElementById('emoji-picker-btn');
        if (picker) {
            picker.classList.add('show');
            this.isOpen = true;
            this.renderEmojis();
        }
        if (btn) {
            btn.classList.add('active');
        }
    }

    // Ð—Ð°ÐºÑ€Ñ‹Ñ‚ÑŒ picker
    close() {
        const picker = document.getElementById('emoji-picker');
        const btn = document.getElementById('emoji-picker-btn');
        if (picker) {
            picker.classList.remove('show');
            this.isOpen = false;
        }
        if (btn) {
            btn.classList.remove('active');
        }
    }

    // ÐŸÐµÑ€ÐµÐºÐ»ÑŽÑ‡Ð¸Ñ‚ÑŒ Ð²Ð¸Ð´Ð¸Ð¼Ð¾ÑÑ‚ÑŒ
    toggle() {
        if (this.isOpen) {
            this.close();
        } else {
            this.open();
        }
    }

    // Ð˜Ð½Ð¸Ñ†Ð¸Ð°Ð»Ð¸Ð·Ð°Ñ†Ð¸Ñ
    init(container, onEmojiSelect) {
        this.onEmojiSelect = onEmojiSelect;

        const picker = this.createPicker();
        container.appendChild(picker);

        // Ð—Ð°ÐºÑ€Ñ‹Ñ‚ÑŒ Ð¿Ñ€Ð¸ ÐºÐ»Ð¸ÐºÐµ Ð²Ð½Ðµ picker
        document.addEventListener('click', (e) => {
            const picker = document.getElementById('emoji-picker');
            const emojiBtn = document.getElementById('emoji-picker-btn');

            if (picker && this.isOpen &&
                !picker.contains(e.target) &&
                e.target !== emojiBtn &&
                !emojiBtn?.contains(e.target)) {
                this.close();
            }
        });

        return this;
    }
